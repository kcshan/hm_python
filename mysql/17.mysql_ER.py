# E-R模型的介绍
# E-R模型即实体-关系模型,E-R模型就是描述数据库存储数据的结构模型

# E-R模型的使用场景
# 1.对于大型公司开发项目，我们需要根据产品经理的设计，我们先使用建模工具,如:power designer,
# db designer等这些软件来画出实体-关系模型(E-R模型)
# 2.然后根据三范式设计数据库表结构

# 说明
# 实体: 用矩形表示, 并标注实体名称
# 属性: 用椭圆表示, 并标注属性名称
# 关系: 用菱形表示, 并标注关系名称

